export const ACCOUNT_LINKING_TRANSLATIONS: Record<AccountLinkingLocale, Catalog> = { de, en, es };

export const getAccountLinkingLocaleTag = (locale: AccountLinkingLocale) => ({
  de: 'de-DE',
  en: 'en-US',
  es: 'es-ES',
})[locale];

export const translateAccountLinkingText = (
  locale: AccountLinkingLocale,
  key: AccountLinkingTranslationKey,
  params: Record<string, string | number> = {},
) => {
  const template = ACCOUNT_LINKING_TRANSLATIONS[locale]?.[key] ?? de[key] ?? key;
  return template.replace(/\{\{(\w+)\}\}/g, (_, token: string) => String(params[token] ?? `{{${token}}}`));
};
