export const BABY_TRANSLATIONS: Record<BabyLocale, Catalog> = { de, en, es };

export const getBabyLocaleTag = (locale: BabyLocale) => ({
  de: 'de-DE',
  en: 'en-US',
  es: 'es-ES',
})[locale];

export const translateBabyText = (
  locale: BabyLocale,
  key: BabyTranslationKey,
  params: Record<string, string | number> = {},
) => {
  const template = BABY_TRANSLATIONS[locale]?.[key] ?? de[key] ?? key;
  return template.replace(/\{\{(\w+)\}\}/g, (_, token: string) => String(params[token] ?? `{{${token}}}`));
};

export const formatBabyAge = (
  locale: BabyLocale,
  age: { years: number; months: number; days: number },
) => {
  const t = (key: BabyTranslationKey) => translateBabyText(locale, key);
  const parts: string[] = [];

  if (age.years > 0) {
    parts.push(`${age.years} ${t(age.years === 1 ? 'unit.year.one' : 'unit.year.other')}`);
  }
  if (age.months > 0 || age.years > 0) {
    parts.push(`${age.months} ${t(age.months === 1 ? 'unit.month.one' : 'unit.month.other')}`);
  }
  parts.push(`${age.days} ${t(age.days === 1 ? 'unit.day.one' : 'unit.day.other')}`);

  return new Intl.ListFormat(getBabyLocaleTag(locale), {
    style: 'long',
    type: 'conjunction',
  }).format(parts);
};
