export const SUBSCRIPTION_TRANSLATIONS: Record<SubscriptionLocale, Catalog> = {
  de,
  en,
  es,
};

export const getSubscriptionLocaleTag = (locale: SubscriptionLocale) => ({
  de: 'de-DE',
  en: 'en-US',
  es: 'es-ES',
})[locale];

export const translateSubscriptionText = (
  locale: SubscriptionLocale,
  key: SubscriptionTranslationKey,
  params: Record<string, string | number> = {},
) => {
  const template = SUBSCRIPTION_TRANSLATIONS[locale]?.[key] ?? de[key] ?? key;
  return template.replace(/\{\{(\w+)\}\}/g, (_, token: string) =>
    String(params[token] ?? `{{${token}}}`),
  );
};

export const getSubscriptionAccessRoleLabel = (
  locale: SubscriptionLocale,
  role: PaywallAccessRole,
) => translateSubscriptionText(locale, `role.${role}`);
