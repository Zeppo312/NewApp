export const SHOPPING_TRANSLATIONS: Record<ShoppingLocale, Catalog> = { de, en, es };

export const getShoppingLocaleTag = (locale: ShoppingLocale) =>
  locale === 'de' ? 'de-DE' : locale === 'es' ? 'es-ES' : 'en-US';

export const translateShoppingText = (
  locale: ShoppingLocale,
  key: string,
  params?: Record<string, string | number>,
): string => {
  const template = SHOPPING_TRANSLATIONS[locale]?.[key] ?? de[key] ?? key;
  if (!params) return template;
  return template.replace(/\{\{(\w+)\}\}/g, (_match, name: string) =>
    params[name] !== undefined ? String(params[name]) : '',
  );
};

export const getShoppingCategoryLabel = (locale: ShoppingLocale, category: string) =>
  translateShoppingText(locale, `category.${category}`);

export const getShoppingLevelLabel = (locale: ShoppingLocale, percent: number) => {
  if (percent >= 75) return translateShoppingText(locale, 'level.full');
  if (percent >= 35) return translateShoppingText(locale, 'level.half');
  if (percent > 0) return translateShoppingText(locale, 'level.low');
  return translateShoppingText(locale, 'level.empty');
};

export const getShoppingUnitLabel = (locale: ShoppingLocale, unit: string) => {
  const normalized = unit.trim().toLocaleLowerCase('de-DE');
  if (['stück', 'stueck', 'piece', 'pieces', 'pcs', 'unidad', 'unidades', 'uds.'].includes(normalized)) {
    return translateShoppingText(locale, 'unit.piece');
  }
  if (['packung', 'packungen', 'package', 'packages', 'paquete', 'paquetes'].includes(normalized)) {
    return translateShoppingText(locale, 'unit.package');
  }
  return unit;
};

export const isShoppingPackageUnit = (unit: string | null | undefined) => {
  const normalized = unit?.trim().toLocaleLowerCase('de-DE') ?? '';
  return ['packung', 'packungen', 'package', 'packages', 'paquete', 'paquetes'].includes(
    normalized,
  );
};

export const formatShoppingQuantity = (
  locale: ShoppingLocale,
  value: number,
  unit: string,
) => formatUnitQuantity(value, getShoppingUnitLabel(locale, unit), getShoppingLocaleTag(locale));

export const formatShoppingDate = (
  locale: ShoppingLocale,
  value: string,
  options: Intl.DateTimeFormatOptions,
) => new Intl.DateTimeFormat(getShoppingLocaleTag(locale), options).format(new Date(value));
